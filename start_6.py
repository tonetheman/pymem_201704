import turtle

# NOT Object oriented
# shows color

# set the shape that represents the turtle
turtle.shape("turtle")

# set the color mode
# only valid choices: 1.0 or 255
turtle.colormode(255)

# change the color to pink
turtle.color(215, 100, 170)

# move forward
turtle.forward(25)

# UI stuff, exit the app on click
turtle.exitonclick()


